export function getCenterComuna( idRegion, idComuna){

    let region = Comunas()[idRegion];
    let center = CENTER_MAP;

    region.forEach( comuna => {

        if( comuna.id == idComuna)
        {   if( "center" in comuna ){
                center = comuna.center;
            }            
        }
    });

    return center;
}
